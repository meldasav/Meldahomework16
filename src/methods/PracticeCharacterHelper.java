package methods;

public class PracticeCharacterHelper {
    public static boolean isSpace1(char c) {
        return c == ' ';//or 32 in ASCII


    }

    public static boolean isDigit1(char c) {
        return c >= 48 && c <= 57;//c>='0' && c<='9'
    }

    public static boolean isUpper1(char c) {
        return c >= 65 && c <= 90; //c>='A' && c<='Z'
    }

    public static boolean isLower1(char c) {
        return c >= 97 && c <= 122;//c>='a' && c<='z'

    }





    }

