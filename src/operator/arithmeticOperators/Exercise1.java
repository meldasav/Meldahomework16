package operator.arithmeticOperators;

public class Exercise1 {
    public static void main(String[] args) {
        int bigNumber=8,smNumber=4;
        int ad=bigNumber+smNumber;
        System.out.println(ad);

        int sub=bigNumber-smNumber;   // or you can do (bigNumber+sumNumber)) write them in paranteses
        System.out.println(sub);

        int div=bigNumber/smNumber;
        System.out.println(div);

        int mul=bigNumber*smNumber;
        System.out.println(mul);
        int rem=bigNumber%smNumber;
        System.out.println(rem);
    }
}
